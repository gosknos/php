class Calculator
{
    function sum($x, $y) {
        return $x + $y;
    }
    function sub($x, $y){
        return $x - $y;
    }
    function multi($x, $y){
        return $x * $y;
    }
    function div($x, $y){
        return $x / $y;
    }
}